// Función para determinar el tipo de cuenta basado en el código
function getTipoCuenta(codigo: string): string {
  const primerDigito = codigo.charAt(0);
  switch (primerDigito) {
    case '1':
      return 'Activo';
    case '2':
      return 'Activo';
    case '3':
      return 'Activo';
    case '4':
      return 'Pasivo';
    case '5':
      return 'Patrimonio';
    case '6':
      return 'Gasto';
    case '7':
      return 'Ingreso';
    case '8':
      return 'Saldos Intermediarios';
    case '9':
      return 'Costos';
    case '0':
      return 'Orden';
    default:
      return 'Otro';
  }
}

// Función para determinar el nivel basado en la longitud del código
function getNivel(codigo: string): number {
  const length = codigo.length;
  if (length <= 2) return 1;
  if (length === 3) return 2;
  if (length === 4) return 3;
  if (length === 5) return 4;
  return 5;
}

